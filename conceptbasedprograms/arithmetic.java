package shivaniprograms;

public class arithmetic {

	public static void main(String[] args) {
		int x=10;
		int y=30;
		System.out.println("addition result is"+(x+y));
		System.out.println("substraction result is"+(x-y));
		System.out.println("multiplication result is"+(x*y));
		System.out.println("division quotient result is"+(x/y));
		System.out.println("division remainder is"+(x%y));


	}

}
